        <div class='page-content'>
            <h2>The Concept</h2>
            <h3>Connect and follow the world</h3>
            <p>
                Le concept de The Whole est de proposer une plateforme qui unifie tout vos services sur une seul et même interface. Réseaux sociaux, flux rss, musique ou tout autre service serait disponible sur la même application et proposerait les mêmes fonctionnalités.
            </p><!--
            !--><p>
                The Whole est pour le moment qu’un prototype, qui a été réalisé avec uniquement 6 services (Facebook, Twitter, Pinterest, Sound Cloud, Last Fm, Flux Rss). Mais il a été pensé que n’importe quel autre service pourrait être intégré.
            </p>   
            <img src='./assets/img/the-whole/the-whole-concept.png' alt=''>
        </div>
    </article>

