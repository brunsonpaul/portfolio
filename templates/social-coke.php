            <h2>The Context</h2>
            <h3>Coca Cola, an active and creative brand</h3>
            <p>
                Year by years, Coca-Cola has shaped its image and reputation thanks to large-scale advertising campaigns, notable for their originality and quality achievements.<br/> 
                The brand has also often worked in collaboration with artists and renowned designers to achieve visual concepts still etched in our minds.
            </p><!--
            !--><p>
                Today, Coca-Cola aims to carry on this tradition of innovative and creative campaigns, staying aware of the new online challenges of 2014.<br/>
                The brand targets a pretty young audience, sensitive to digital innovations, to strengthen its notoriety and its image as a state-of-the-art brand.
            </p>
            <img id='coca-cola-logo' src='assets/img/social-coke/coca-cola.svg' alt='Coca Cola logo'>   
